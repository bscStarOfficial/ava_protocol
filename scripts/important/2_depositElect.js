const {ethers} = require("hardhat");
const {BigNumber} = require("bignumber.js");
let bitMiner;
// npx hardhat run scripts/important/2_depositElect.js --network bsc
(async () => {
  bitMiner = await ethers.getContract("BitMiner");

  let users = getUsers();
  let depositUsers = [];
  let depositElect = [];

  for (const user of users) {
    let miner = await bitMiner.miners(user);
    let day30 = new BigNumber(getElectricityPerSec(miner.lv)).multipliedBy(86400).multipliedBy(30);

    console.log(user, miner.lv);
    if (miner.lv == 0) {
      let secondToFirst = await bitMiner.secondToFirst(user);
      console.log({secondToFirst})
      process.exit(0);
    }
    depositUsers.push(user);
    depositElect.push(day30.toFixed());
  }

  let tx = await bitMiner.electDeposit(depositUsers, depositElect);
  console.log(tx.hash);
  await tx.wait();

  process.exit(0);
})();

