async function getLv(user) {
  let length = await communityDividend2.getRealLength(user);
  return length.add(3).toNumber()
}

async function sleep(time) {
  await new Promise((resolve, reject) => {
    setTimeout(() => {
      resolve()
    }, time)
  })
}
